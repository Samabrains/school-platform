/**
 * Bundles core-school-template drizzle SQL for worker-side D1 apply.
 * Run after template schema changes: node scripts/bundle-tenant-migrations.mjs
 */
import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, "..", "..");
const TEMPLATE_ROOT = process.env.TEMPLATE_ROOT
  ? path.resolve(ROOT, process.env.TEMPLATE_ROOT)
  : fs.existsSync(path.join(ROOT, "drizzle"))
    ? ROOT
    : path.join(ROOT, "core-school-template");
const DRIZZLE = path.join(TEMPLATE_ROOT, "drizzle");
const OUT = path.resolve(__dirname, "..", "src", "data", "tenant-d1-migrations.ts");

const files = fs
  .readdirSync(DRIZZLE)
  .filter((f) => f.endsWith(".sql"))
  .sort();

const statements = [];
for (const file of files) {
  const sql = fs.readFileSync(path.join(DRIZZLE, file), "utf-8");
  for (const chunk of sql.split(/--> statement-breakpoint/)) {
    const stmt = chunk.trim();
    if (stmt) statements.push(stmt);
  }
}

const out = `/** Auto-generated by scripts/bundle-tenant-migrations.mjs — do not edit */\nexport const TENANT_D1_STATEMENTS: string[] = ${JSON.stringify(statements, null, 2)};\n`;

fs.mkdirSync(path.dirname(OUT), { recursive: true });
fs.writeFileSync(OUT, out);
console.log(`Wrote ${statements.length} statements to ${OUT}`);
